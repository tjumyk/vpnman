export const STORAGE_KEY = 'vpnman-locale'
export type Locale = 'en' | 'zh-Hans'
export type TranslationParams = Record<string, string | number>

export function formatTranslation(template: string, params?: TranslationParams): string {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, key: string) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : `{${key}}`,
  )
}

export function getDefaultLocale(): Locale {
  const stored = localStorage.getItem(STORAGE_KEY)
  if (stored === 'zh-Hans' || stored === 'en') return stored
  return navigator.language.toLowerCase().startsWith('zh') ? 'zh-Hans' : 'en'
}

